CREATE TABLE grade (
    grade_code VARCHAR(10) PRIMARY KEY,
    grade_name VARCHAR(50),
    promotion_duration INT,
    salary INT
);

CREATE TABLE `position` (
    position_code VARCHAR(10) PRIMARY KEY,
    position_name VARCHAR(100),
    position_level INT
);

CREATE TABLE employee (
    employee_code VARCHAR(10) PRIMARY KEY,
    name VARCHAR(100),
    employee_number INT,
    hire_date DATE,
    employment_status VARCHAR(20),
    gender VARCHAR(10),
    grade_code VARCHAR(10),
    position_code VARCHAR(10)
);

CREATE TABLE attendance (
    attendance_code VARCHAR(10) PRIMARY KEY,
    date DATE, 
    check_in_time TIME,
    check_out_time TIME,
    attendance_status VARCHAR(20),
    employee_code VARCHAR(10)
);

CREATE TABLE task (
    task_code VARCHAR(10) PRIMARY KEY,
    task_name VARCHAR(100),
    date DATE, 
    target VARCHAR(50),
    achievement VARCHAR(50),
    employee_code VARCHAR(10)
);

